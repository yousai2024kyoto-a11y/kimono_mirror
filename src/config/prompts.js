export function buildYukataPrompt({ gender, obiColor, background }) {
  const bgText = BACKGROUND_PROMPTS[background] ?? BACKGROUND_PROMPTS.style_studio;
  const person = GENDER_CONFIG[gender] ?? GENDER_CONFIG.default;
  const garmentType = "「浴衣（夏着物）と帯」";
  const obiColorInfo = obiColor !== "auto" ? `帯の色は ${obiColor}。` : "";

  return (
    "あなたはプロのAIファッションスタイリスト兼フォトエディターです。" +
    "入力画像を元に、超高画質な写真を生成してください。" +
    `現在、画像の人物は手に${garmentType}を持っています。` +
    `あなたの任務は、その手に持っている${garmentType}を、実際にその人物に着せることです。` +
    `被写体：${person.subject}、${person.style}。` +
    `背景：${bgText}。` +
    "\n【重要要件（必ず守ること）】:\n" +
    "1. 顔と髪型の完全維持（最優先事項）: \n" +
    "   - 入力画像の人物の顔立ち、表情、特徴を厳密に維持してください。\n" +
    "   - 髪型と髪色を入力画像そのまま維持してください。髪の長さや質感を勝手に変更しないでください。\n" +
    "   - 丈は足元まで生成してください。\n" +
    "2. 生地の再現: \n" +
    `   - 人物が手に持っている布地や衣服の「柄・色・質感」を分析し、それを着用後の着物に正確に適用してください。` +
    "   - 手に持っている布と全く違う柄を捏造しないでください。\n" +
    `3. 帯（オビ）の仕様 - ${person.obiTerm}: \n` +
    `   - ${person.obiInstruction}\n` +
    `   - ${obiColorInfo} 手に帯を持っている場合は、その色や柄を必ず使用してください。\n` +
    "4. ポーズと手: \n" +
    "   - 人物は自然な立ち姿にしてください。\n" +
    "   - 服をすでに「着ている」状態なので、手には何も持たせず、体の横に自然に下ろすか、お腹の前で軽く組ませてください。\n" +
    "   - 下駄を履かせてください。\n" +
    "5. 品質設定:\n" +
    "   - 8k解像度、写真のようなリアルな質感、映画のようなライティング、高精細。\n" +
    "6. 構図・向き:\n" +
    "   - 必ず縦長（ポートレート）構図で生成してください。\n" +
    "   - 人物の全身が縦向きの画面内に収まるよう、やや引いたショットにしてください。\n" +
    "   - 人物を画面中央に配置してください。"
  );
}
